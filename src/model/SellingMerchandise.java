package model;

public class SellingMerchandise {
	int id;
	int stock;

	public SellingMerchandise() {
	}

	public int getStock() {
		return stock;
	}

	public void setStock(int stock) {
		this.stock = stock;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

}
